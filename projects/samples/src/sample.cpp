#include <sample.hpp>

//------------------------------------------------------------------------------------------------------------------------
bool cgame::init()
{
	m_world = kokoro::world::create("samples");
	kokoro::world::promote(*m_world);
	return true;
}

//------------------------------------------------------------------------------------------------------------------------
void cgame::post_init()
{

}

//------------------------------------------------------------------------------------------------------------------------
void cgame::shutdown()
{
	kokoro::world::destroy("samples");
	m_world = nullptr;
}

//------------------------------------------------------------------------------------------------------------------------
void cgame::update(float dt)
{
	kokoro::world::tick(*m_world, dt);
}
